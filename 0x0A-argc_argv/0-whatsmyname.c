#include "main.h"
#include <stdio.h>

/**
 * whatsmyname - Print the name of the program
 * @argc: Program argument count
 * @argv: Program argument value
 * Return: Nothing
 */

void whatsmyname(int argc, char *argv[])
{
	if (argc == 0)
		printf("%s\n", argv[0]);
	else
		printf("%s\n", argv[0]);
}
